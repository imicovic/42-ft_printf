#include "../include/ft_printf.h"
#include "../include/libft.h"

int	ft_print_str(char *s)
{
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	write(1, s, ft_strlen(s));
	return (ft_strlen(s));
}
